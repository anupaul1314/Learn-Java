public class DataTypes {
    public static void main(String[] args) {
        /*
        * byte - 1 [-128 to 127]
        * short - 2
        * int - 4 1,2,3
        * long - 8
        * float - 4 3.14
        * double - 8
        * char - 2 a,b,c,d
        * boolean - 1 true/false  */
        //Primitive Datatypes

        byte age = 30;
        int phone = 0023323243443;
        long newPhone = 982093199018913102L;
        float pi = 3.14F;
        char letter = 'a';
        boolean isAdult = true;

        //Non-Primitive Datatypes

        String name = "Any";
        String friend = "Adam";
        System.out.println(name.length());  //ANY
    }
}
